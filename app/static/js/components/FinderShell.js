import { Toolbar } from "./toolbar/Toolbar.js";
import { SidebarNav } from "./sidebar/SidebarNav.js";
import { MyEdits } from "./sidebar/MyEdits.js";
import { VaultFileList } from "./browser/FileList.js";
import { InfoDock } from "./info-dock/InfoDock.js";

const h = React.createElement;

export function FinderShell({
  folder,
  breadcrumbs,
  myEdits,
  folderChildren,
  subfolders,
  files,
  selectedId,
  contentsItems,
  contentsSort,
  contentsSelection,
  folderItems,
  folderSelection,
  selectedDoc,
  selectionItems,
  searchQuery,
  recursiveSearch,
  dropHint,
  uploadHover,
  draggingId,
  draggingFolderPath,
  currentUser,
  isAdmin,
  canGoBack,
  canGoForward,
  canGoUp,
  onNavigateBack,
  onNavigateForward,
  onNavigateUp,
  onSelectFolder,
  onSelectDoc,
  onSelectContentItem,
  onSelectFolderItem,
  onSearchQueryChange,
  onRecursiveSearchChange,
  onContentsSortChange,
  onClearSelection,
  onOpenDoc,
  onDropOnFolder,
  onClearDropHint,
  onCanvasDrop,
  onCanvasDragOver,
  onCanvasDragLeave,
  onFileDragStart,
  onFileDragEnd,
  onFolderDragStart,
  onFolderDragEnd,
  onFileContextMenu,
  onFolderContextMenu,
  onMyEditContextMenu,
  onPageContextMenu,
  inlineFolderDraft,
  onInlineFolderNameChange,
  onCommitInlineFolder,
  onCancelInlineFolder,
  onTriggerUpload,
  logoutUrl,
  onOpenSettings,
  settingsButtonRef,
  onDownload,
  onDownloadSelection,
  onDownloadVersion,
  onLock,
  onLockSelection,
  onRename,
  onMove,
  onMoveSelection,
  onStartEdit,
  onRelease,
  onReleaseSelection,
  onSave,
  onArchive,
  onArchiveSelection,
  onUnarchive,
  onRestoreSelection,
  onPermanentDelete,
  onDeleteSelection,
  onOpenFolder,
  busy,
}) {
  return h(
    "div",
    { className: "finder-shell", onContextMenu: onPageContextMenu },
    h(Toolbar, {
      folder,
      breadcrumbs,
      canGoBack,
      canGoForward,
      canGoUp,
      onNavigateBack,
      onNavigateForward,
      onNavigateUp,
      logoutUrl,
      onOpenSettings,
      settingsButtonRef,
      onSelectFolder,
      onDropOnFolder,
      onClearDrop: onClearDropHint,
    }),
    h(
      "div",
      { className: "finder-frame" },
      h(
        "aside",
        { className: "finder-sidebar" },
        h(SidebarNav, {
          currentFolder: folder,
          folderChildren,
          folderItems,
          selectedKeys: folderSelection,
          dropHint,
          onSelect: onSelectFolder,
          onSelectItem: onSelectFolderItem,
          onContextMenu: onFolderContextMenu,
          onFolderDragStart,
          onFolderDragEnd,
          draggingFolderPath,
          onDropOnFolder,
          onClearDropHint,
        }),
        h(MyEdits, {
          edits: myEdits,
          selectedId,
          onSelect: (doc) => {
            onSelectFolder(doc.folder || "");
            onSelectDoc(doc.id);
          },
          onContextMenu: onMyEditContextMenu,
        })
      ),
      h(VaultFileList, {
        folder,
        subfolders,
        files,
        currentUser,
        selectedKeys: contentsSelection,
        orderedItems: contentsItems,
        sort: contentsSort,
        searchQuery,
        recursiveSearch,
        draggingId,
        draggingFolderPath,
        dropHint,
        uploadHover,
        onSelectFolder,
        onSelectItem: onSelectContentItem,
        onSearchQueryChange,
        onRecursiveSearchChange,
        onSortChange: onContentsSortChange,
        onBackgroundClick: onClearSelection,
        onOpenFile: onOpenDoc,
        onFileDragStart,
        onFileDragEnd,
        onFolderDragStart,
        onFolderDragEnd,
        onFileContextMenu,
        onFolderContextMenu,
        inlineFolderDraft,
        onInlineFolderNameChange,
        onCommitInlineFolder,
        onCancelInlineFolder,
        onDropOnFolder,
        onClearDropHint,
        onCanvasDrop,
        onCanvasDragOver,
        onCanvasDragLeave,
        onUploadClick: onTriggerUpload,
      }),
      h(InfoDock, {
        doc: selectedDoc,
        selectionItems,
        currentUserId: currentUser.id,
        onDownload,
        onDownloadSelection,
        onDownloadVersion,
        onLock,
        onLockSelection,
        onRename,
        onMove,
        onMoveSelection,
        onStartEdit,
        onRelease,
        onReleaseSelection,
        onSave,
        onArchive,
        onArchiveSelection,
        onUnarchive,
        onRestoreSelection,
        onPermanentDelete,
        onDeleteSelection,
        onOpenFolder,
        busy,
        isAdmin,
      })
    )
  );
}
