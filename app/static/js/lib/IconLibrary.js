const iconsById = new Map(iconLibrary.map((item) => [item.id, item.icon]));

export function findIconDefinition(iconId, fallback = "folder") {
  return iconsById.get(iconId) || utilityIcons.get(iconId) || iconsById.get(fallback) || faFolder;
}

export function findIconByKeyword(keyword) {
  const words = (keyword || "").trim().toLowerCase().split(/\s+/).filter(Boolean);
  if (!words.length) {
    return "";
  }
  const match = iconLibrary.find((item) => {
    const haystack = `${item.id} ${item.label} ${item.keywords}`.toLowerCase();
    return words.some((word) => haystack.includes(word));
  });
  return match?.id || "";
}

export function searchIcons(query) {
  const needle = (query || "").trim().toLowerCase();
  if (!needle) {
    return iconLibrary;
  }
  return iconLibrary.filter((item) =>
    `${item.id} ${item.label} ${item.keywords}`.toLowerCase().includes(needle)
  );
}
