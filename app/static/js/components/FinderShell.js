import { Toolbar } from "./toolbar/Toolbar.js";
import { SidebarNav } from "./sidebar/SidebarNav.js";
import { MyEdits } from "./sidebar/MyEdits.js";
import { VaultFileList } from "./browser/FileList.js";
import { InfoDock } from "./info-dock/InfoDock.js";

const h = React.createElement;

export function FinderShell({
  folder,
  breadcrumbs,
  myEdits,
  folderChildren,
  subfolders,
  files,
  selectedId,
  selectedDoc,
  dropHint,
  uploadHover,
  draggingId,
  draggingFolderPath,
  currentUser,
  isAdmin,
  canGoBack,
  canGoForward,
  canGoUp,
  onNavigateBack,
  onNavigateForward,
  onNavigateUp,
  onSelectFolder,
  onSelectDoc,
  onClearSelection,
  onOpenDoc,
  onDropOnFolder,
  onClearDropHint,
  onCanvasDrop,
  onCanvasDragOver,
  onCanvasDragLeave,
  onFileDragStart,
  onFileDragEnd,
  onFolderDragStart,
  onFolderDragEnd,
  onFileContextMenu,
  onFolderContextMenu,
  onMyEditContextMenu,
  onPageContextMenu,
  inlineFolderDraft,
  onInlineFolderNameChange,
  onCommitInlineFolder,
  onCancelInlineFolder,
  onTriggerUpload,
  logoutUrl,
  onDownload,
  onDownloadVersion,
  onLock,
  onRename,
  onMove,
  onStartEdit,
  onRelease,
  onSave,
  onArchive,
  onUnarchive,
  onPermanentDelete,
  onOpenFolder,
  busy,
}) {
  return h(
    "div",
    { className: "finder-shell", onContextMenu: onPageContextMenu },
    h(Toolbar, {
      folder,
      breadcrumbs,
      canGoBack,
      canGoForward,
      canGoUp,
      onNavigateBack,
      onNavigateForward,
      onNavigateUp,
      logoutUrl,
      onSelectFolder,
      onDropOnFolder,
      onClearDrop: onClearDropHint,
    }),
    h(
      "div",
      { className: "finder-frame" },
      h(
        "aside",
        { className: "finder-sidebar" },
        h(SidebarNav, {
          currentFolder: folder,
          folderChildren,
          dropHint,
          onSelect: onSelectFolder,
          onDropOnFolder,
          onClearDropHint,
        }),
        h(MyEdits, {
          edits: myEdits,
          selectedId,
          onSelect: (doc) => {
            onSelectFolder(doc.folder || "");
            onSelectDoc(doc.id);
          },
          onContextMenu: onMyEditContextMenu,
        })
      ),
      h(VaultFileList, {
        folder,
        subfolders,
        files,
        currentUser,
        selectedId,
        draggingId,
        draggingFolderPath,
        dropHint,
        uploadHover,
        onSelectFolder,
        onSelectFile: onSelectDoc,
        onBackgroundClick: onClearSelection,
        onOpenFile: onOpenDoc,
        onFileDragStart,
        onFileDragEnd,
        onFolderDragStart,
        onFolderDragEnd,
        onFileContextMenu,
        onFolderContextMenu,
        inlineFolderDraft,
        onInlineFolderNameChange,
        onCommitInlineFolder,
        onCancelInlineFolder,
        onDropOnFolder,
        onClearDropHint,
        onCanvasDrop,
        onCanvasDragOver,
        onCanvasDragLeave,
        onUploadClick: onTriggerUpload,
      }),
      h(InfoDock, {
        doc: selectedDoc,
        currentUserId: currentUser.id,
        onDownload,
        onDownloadVersion,
        onLock,
        onRename,
        onMove,
        onStartEdit,
        onRelease,
        onSave,
        onArchive,
        onUnarchive,
        onPermanentDelete,
        onOpenFolder,
        busy,
        isAdmin,
      })
    )
  );
}
