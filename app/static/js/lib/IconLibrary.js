function labelFromIconName(iconName) {
  return iconName
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function iconEntry(icon, label = "", keywords = "") {
  return {
    icon,
    id: icon.iconName,
    keywords,
    label: label || labelFromIconName(icon.iconName),
  };
}

